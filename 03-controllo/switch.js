let giorno = "Lunedì";

switch (giorno) {
  case "Lunedì":
    console.log("Inizio settimana!");
    break;
  case "Venerdì":
    console.log("Quasi weekend!");
    break;
  default:
    console.log("Giorno normale");
}

// case → ogni possibile valore

// break → esce dallo switch

// default → se nessun case corrisponde