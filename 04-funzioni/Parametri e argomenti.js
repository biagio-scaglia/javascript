// Parametri e argomenti

// Le funzioni possono ricevere valori da usare all’interno:
function salutaPersona(nome) {
  console.log("Ciao " + nome + "!");
}

salutaPersona("Biagio"); // Ciao Biagio!
salutaPersona("Morena"); // Ciao Morena!

// nome → parametro della funzione

// "Biagio" e "Morena" → argomenti passati quando chiami la funzione