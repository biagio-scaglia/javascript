// | Operatore | Esempio     | Significato               |
// | --------- | ----------- | ------------------------- |
// | `==`      | `5 == "5"`  | uguale (con coercizione)  |
// | `===`     | `5 === "5"` | uguale e stesso tipo      |
// | `!=`      | `5 != "6"`  | diverso (con coercizione) |
// | `!==`     | `5 !== 5`   | diverso o tipo diverso    |
// | `>`       | `5 > 3`     | maggiore                  |
// | `<`       | `5 < 3`     | minore                    |
// | `>=`      | `5 >= 5`    | maggiore o uguale         |
// | `<=`      | `5 <= 3`    | minore o uguale           |
